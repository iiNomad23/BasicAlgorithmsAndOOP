// -----------------------------------------------------------------------------------------------------------------------------

// function definitions --------------------------------------------------------------------------------------------------------

int recursiveCalc(int n) {
	if (n <= 0) {
		return 0;
	}
	else {
		return recursiveCalc(n - 1) + n;
	}
}

